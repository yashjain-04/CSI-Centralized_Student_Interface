package dto;

/**
 *
 * @author ydhup
 */
public class HodDTO {
    String hodId, hodName, hodDepartment, hodContact, hodCity, password, role;

    public String getHodId() {
        return hodId;
    }

    public void setHodId(String hodId) {
        this.hodId = hodId;
    }

    public String getHodName() {
        return hodName;
    }

    public void setHodName(String hodName) {
        this.hodName = hodName;
    }

    public String getHodDepartment() {
        return hodDepartment;
    }

    public void setHodDepartment(String hodDepartment) {
        this.hodDepartment = hodDepartment;
    }

    public String getHodContact() {
        return hodContact;
    }

    public void setHodContact(String hodContact) {
        this.hodContact = hodContact;
    }

    public String getHodCity() {
        return hodCity;
    }

    public void setHodCity(String hodCity) {
        this.hodCity = hodCity;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
